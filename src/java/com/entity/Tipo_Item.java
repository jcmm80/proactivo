        if (!(object instanceof Tipo_Item)) {
            return false;
        }
        Tipo_Item other = (Tipo_Item) object;
        if ((this.getId() == null && other.getId() != null) || (this.getId() != null && !this.id.equals(other.id))) {
            return false;
        }
